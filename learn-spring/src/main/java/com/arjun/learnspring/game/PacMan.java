package com.arjun.learnspring.game;

public class PacMan implements GamingConsole {
	public void up() {
		System.out.println("Going Up");
	}
	public void down() {
		System.out.println("Going Down");
	}
	public void left() {
		System.out.println("Going Left");
	}
	public void right() {
		System.out.println("Going Right");
	}
}
